#include <iostream>


int main() {

int* ptr = new int; // динамічне виділення памяті
*ptr = 8; // записуємо знаечння

delete ptr; // звільняємо память. ptr теперь висячй вказівник

std::cout << *ptr; // розіменування можливе, але вже небезпечне
delete ptr; // повторне звільнення також неебезпечне

return 0;
}
